<?php

class Image extends CI_Controller
{

	//////////////////////////////////////////////////////////////////////////////////////////////

	public function __construct()
	{
		parent::__construct();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////

	function deleteImage($intArticleID)
	{
		$intImageID = $this->input->get('intImageID', true);

		$this->load->model('image_model');

		$this->image_model->deleteImage($intImageID);

		redirect(base_url() . 'index.php/article/edit/' . $intArticleID);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////
}
?>
